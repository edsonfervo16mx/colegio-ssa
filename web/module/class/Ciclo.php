		public function listar($key){
			$dataBase = new dbMysql;
			$dataBase->connectDB($key);
			$sql = 'SELECT ciclo.cve_ciclo,ciclo.nombre_ciclo,ciclo.inicio_ciclo,ciclo.fin_ciclo,ciclo.cve_campus from ciclo where ciclo.status_ciclo = "active"';
			$res = $dataBase->triggerSimple($key,$sql);
			$i=0;
			$line = null;
			while ($row = mysqli_fetch_assoc($res)) {
				$line[$i] = array_map('utf8_encode', $row) ;
				$i++;
			}
			$data = json_encode($line);
			$data = json_decode($data);
			return ($data);
		}
	}
